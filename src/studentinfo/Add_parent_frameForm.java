    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        boolean validated = true;

        if (validated && checkNullInputs()) {
            validated = false;
            JOptionPane.showMessageDialog(null, "Error: Please ensure that you input all the necessary information");
        }
        if (validated && validCell() == false) {
            validated = false;
            JOptionPane.showMessageDialog(null, "Error: Please ensure that you provide a valid contact number");
        }
        if(validated && validNumKids()==false){
            validated = false;
        }

        if (validated) {
                //get all the text from text fields.
                String name = jTextField1.getText();
                String Sname = jTextField2.getText();
                String contactNum = jTextField3.getText();
                String address = jTextField4.getText();
                int num = Integer.parseInt(jTextField5.getText());
                
                //create a new parent object
                Parent myParent = new Parent(name, Sname, contactNum, address, num);
                //add parent to database
                myParent.addParent(myParent);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

