    public boolean checkNullInputs() {
        return (jTextField1.getText().equals("")
                || jTextField2.getText().equals("")
                || jTextField3.getText().equals("")
                || jTextField4.getText().equals("")
                || jTextField5.getText().equals(""));
    }

    public boolean validCell() {
        String number = jTextField3.getText();
        if (number.length() != 10) {
            return false;
        }
        return true;
    }

    public boolean validNumKids() {
        try{
          int num = Integer.parseInt(jTextField5.getText());
          return (num<10 && num>0);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error: You cannot enroll more than 10 kids or less than 1");
            return false;
        }        
    }

